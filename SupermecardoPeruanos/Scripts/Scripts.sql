CREATE PROCEDURE [dbo].[CATALOGO_PERSONALIZADO_Q01]
  @CATEGORIAS AS dbo.CategoryList READONLY,
  @CANTIDAD INTEGER,
  @PAGINA INTEGER
AS
BEGIN
  SELECT * FROM ( 
	 SELECT [COD_PROD_CANJ]
      ,[NOM_PROD_CANJ]
      ,[COD_CATE_PROD]
      ,[VAL_CANJ]
      ,[TIP_CANJ]
      ,[DES_PROD_CANJ]
	  ,[IMAGEN]
	  ,COUNT(*) OVER() as TOTAL
      ,Row_number() OVER(ORDER BY COD_PROD_CANJ) AS RowNum 
	  FROM dbo.PRODUCTO_CANJE 
	  WHERE [COD_CATE_PROD] IN (SELECT ID FROM @CATEGORIAS)
   ) T
   WHERE T.RowNum BETWEEN ( @PAGINA - 1 ) * @PAGINA + 1 
   AND @PAGINA * @CANTIDAD
END
GO

