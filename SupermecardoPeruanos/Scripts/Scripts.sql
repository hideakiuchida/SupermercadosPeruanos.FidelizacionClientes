create Procedure [dbo].[PRODUCTO_Q01]
/*------------------------------------------------------*/  

/*------------------------------------------------------*/  
@COD_PROD INTEGER
  
As  
BEGIN

IF @COD_PROD = 0
BEGIN
	SELECT T2.COD_PROD_CANJ, T2.IMAGEN 'IMAGEN', T2.VAL_CANJ , T1.CAN_STOC_CANJ,
	T2.NOM_PROD_CANJ, T2.DES_PROD_CANJ, T2.COD_CATE_PROD , T3.NOM_CATE_PROD ,T2.CON_PROD_CANJ,T2.CON_PROD_CANJ
	FROM STOCK_CANJE T1 INNER JOIN PRODUCTO_CANJE T2 ON
	( T2.COD_PROD_CANJ = T1.COD_STOC_ITEM AND T2.COD_CATE_PROD = T1.COD_STOC_CATE )
	INNER JOIN CATEGORIA T3 ON
	( T3.COD_CATE_PROD = T2.COD_CATE_PROD )
	ORDER BY T1.COD_STOC_ITEM
END
ELSE
BEGIN 
	SELECT T2.COD_PROD_CANJ, T2.IMAGEN 'IMAGEN', T2.VAL_CANJ , T1.CAN_STOC_CANJ,
	T2.NOM_PROD_CANJ, T2.DES_PROD_CANJ, T2.COD_CATE_PROD , T3.NOM_CATE_PROD ,T2.CON_PROD_CANJ,T2.CON_PROD_CANJ
	FROM STOCK_CANJE T1 INNER JOIN PRODUCTO_CANJE T2 ON
	( T2.COD_PROD_CANJ = T1.COD_STOC_ITEM AND T2.COD_CATE_PROD = T1.COD_STOC_CATE )
	INNER JOIN CATEGORIA T3 ON
	( T3.COD_CATE_PROD = T2.COD_CATE_PROD )
	WHERE T1.COD_STOC_ITEM = @COD_PROD
	ORDER BY T1.COD_STOC_ITEM
END
END
